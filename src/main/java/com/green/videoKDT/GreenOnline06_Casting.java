package com.green.videoKDT;

public class GreenOnline06_Casting {
    public static void main(String[] args){
        byte b=127;
        int i = 100;
        System.out.println(b+i);//int
        System.out.println(10/4);//int
        System.out.println(10.0/4);//double
        System.out.println((int)2.9+1.8);//double
                            //2+1.8=3.8
        System.out.println((int)(2.9+1.8));//int
                            //(int)4.7=4
        System.out.println((int)2.9+(int)1.8);//double
                                //2+1=3

    }
}
